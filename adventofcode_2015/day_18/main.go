package main

import (
	"fmt"

	"github.com/mazzegi/adventofcode/adventofcode_2015/lights"
)

func main() {
	test := false
	var in string
	var dim int
	var steps int
	if test {
		in = inputTest
		dim = 6
		steps = 5
	} else {
		in = input
		dim = 100
		steps = 100
	}
	g := lights.NewGrid(dim, dim)
	g.PopulateFromString(in)
	g.TurnOnEdges()
	fmt.Printf("initially %d lights are on\n", g.LightsOn())

	for i := 0; i < steps; i++ {
		g.Next()
		fmt.Printf("after %d steps %d lights are on\n", i+1, g.LightsOn())
	}
	fmt.Printf("after %d steps %d lights are on\n", steps, g.LightsOn())
}

