package day_04

import (
	"testing"

	"github.com/mazzegi/adventofcode/testutil"
)

const inputTest = `
2-4,6-8
2-3,4-5
5-7,7-9
2-8,3-7
6-6,4-6
2-6,4-8
`

func TestPart1MainFunc(t *testing.T) {
	res, err := part1MainFunc(inputTest)
	testutil.CheckUnexpectedError(t, err)
	var exp int = 2
	if exp != res {
		t.Fatalf("want %d, have %d", exp, res)
	}
}

